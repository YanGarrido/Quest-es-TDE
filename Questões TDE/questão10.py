# Leia uma velocidade em km/h (quilômetros por hora) e apresente-a convertida em m/s (metros por segundo). A formula de conversão é: M = K/3.6, sendo K a velocidade em km/h e M em m/s.
K = input("Digite uma velocidade em km/h: " )
K = float(K)
M = K/3.6
print(" A converção de ", K, "km/h para m/s é: ", M )
