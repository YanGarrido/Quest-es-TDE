# Leia um valor de área em hectares e apresente-o convertido em metros quadrados m2. A formula de conversão é: M = H ∗10000, sendo M a área em metros quadrados e H a área em hectares.
H = input("Escreva uma área em hectares: ")
H = float(H)
M = H * 10000
input(f"Sua área em metros quadrados é: {M}")
